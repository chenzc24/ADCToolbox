def test_ntf_analyzer_bandpass():
    """Test ntf_analyzer with bandpass NTF.

    Validates bandpass NTF centered around normalized frequency 0.25.
    NTF(z) = 1 + z^-2 creates a notch at DC and Fs/2, peak at Fs/4.
    """
